const DICT: Record<Locale, Dict> = { en, zh };

export function t(locale: Locale, key: string, ...args: (string | number)[]): string {
  let str = DICT[locale][key] ?? DICT["en"][key] ?? key;
  args.forEach((a, i) => { str = str.replace(`{${i}}`, String(a)); });
  return str;
}

export function useT() {
  const locale = useAppStore(state => state.locale);
  return (key: string, ...args: (string | number)[]) => t(locale, key, ...args);
}

export function detectLocale(): Locale {
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}
